#[test]
fn selected_session_id_matches_fixture() {
    let (db, _dir) = open_fixture_copy("selected_session_id");
    assert_eq!(
        db.selected_session_id().unwrap(),
        Some("11111111-1111-1111-1111-111111111111".to_string())
    );
}

#[test]
fn app_state_entries_drops_the_real_grdb_written_null_row() {
    let (db, _dir) = open_fixture_copy("app_state_entries");
    let entries = db.app_state_entries("tab.pane.layout.").unwrap();
    assert_eq!(
        entries.len(),
        3,
        "the NULL-valued tab.pane.layout.33333333... row must be dropped, not the other 3"
    );
    assert_eq!(
        entries.get("tab.pane.layout.11111111-1111-1111-1111-111111111111"),
        Some(&"{\"paneKind\":\"terminal\",\"paneTitle\":\"端末\"}".to_string())
    );
    assert_eq!(
        entries.get("tab.pane.layout.22222222-2222-2222-2222-222222222222"),
        Some(&"{\"paneKind\":\"terminal\",\"paneTitle\":\"記号!@#$%^&*()_+-=\"}".to_string())
    );
    // Empty string is a real value, distinct from the dropped NULL row.
    assert_eq!(
        entries.get("tab.pane.layout.empty-string"),
        Some(&String::new())
    );
    assert_eq!(
        entries.get("tab.pane.layout.33333333-3333-3333-3333-333333333333"),
        None
    );
    // Outside the prefix -- must not leak in.
    assert!(!entries.contains_key("other.unrelated.key"));
}

#[test]
fn app_state_on_a_real_null_valued_key_is_none_not_the_key_missing() {
    let (db, _dir) = open_fixture_copy("app_state_null");
    // The key exists (GRDB wrote a row for it with value = NULL); fetching
    // it must still report None, matching GRDB's fetchOne NULL-collapsing.
    assert_eq!(
        db.app_state("tab.pane.layout.33333333-3333-3333-3333-333333333333")
            .unwrap(),
        None
    );
    assert_eq!(
        db.app_state("other.unrelated.key").unwrap(),
        Some("unrelated-value".to_string())
    );
    assert_eq!(db.app_state("no-such-key-at-all").unwrap(), None);
}

#[test]
fn never_touches_grdb_migrations_across_a_full_read_write_delete_cycle() {
    let (db, path) = open_fixture_copy("grdb_migrations_untouched");

    let before = raw_grdb_migrations(&path);
    assert_eq!(before, vec!["v1", "v2-agentSession", "v3-adapter"]);

    // Exercise every write operation, not just reads.
    db.upsert(&SessionRecord::new(
        "55555555-5555-5555-5555-555555555555",
        "/tmp/new",
        "new session",
        None,
        utc(2027, 3, 4, 5, 6, 7, 42),
        4,
        None,
        None,
        None,
    ))
    .unwrap();
    db.delete_session("44444444-4444-4444-4444-444444444444")
        .unwrap();
    db.set_app_state(Some("v"), "some.new.key").unwrap();
    db.set_selected_session_id(Some("55555555-5555-5555-5555-555555555555"))
        .unwrap();
    drop(db);

    let after = raw_grdb_migrations(&path);
    assert_eq!(
        before, after,
        "grdb_migrations must be byte-identical before/after"
    );
}

#[test]
fn upsert_writes_grdb_exact_date_format_and_round_trips() {
    let (db, path) = open_fixture_copy("upsert_date_format");
    let record = SessionRecord::new(
        "66666666-6666-6666-6666-666666666666",
        "/tmp/six",
        "six",
        None,
        utc(2027, 3, 4, 5, 6, 7, 42),
        99,
        None,
        None,
        None,
    );
    db.upsert(&record).unwrap();
    drop(db);

    // Read the raw TEXT back with a bare rusqlite connection -- this must
    // be exactly what GRDB's `DateFormatter` would have produced
    // ("yyyy-MM-dd HH:mm:ss.SSS", UTC, always 3 fractional digits), so a
    // real Swift `SessionDatabase` opening this same file would decode it
    // identically.
    let conn = rusqlite::Connection::open(&path).unwrap();
    let raw: String = conn
        .query_row(
            "SELECT addedAt FROM session WHERE id = '66666666-6666-6666-6666-666666666666'",
            [],
            |row| row.get(0),
        )
        .unwrap();
    assert_eq!(raw, "2027-03-04 05:06:07.042");

    // And reopening through our own port reads it back identically.
    let db2 = SessionDatabase::open(&path).unwrap();
    let reread = db2
        .all_sessions()
        .unwrap()
        .into_iter()
        .find(|s| s.id == "66666666-6666-6666-6666-666666666666")
        .unwrap();
    assert_eq!(reread, record);
}

#[test]
fn upsert_on_existing_fixture_row_updates_not_duplicates() {
    let (db, _dir) = open_fixture_copy("upsert_update");
    let mut updated = expected_sessions()[0].clone();
    updated.name = "改名後".to_string();
    updated.sort_order = 100;
    db.upsert(&updated).unwrap();

    let all = db.all_sessions().unwrap();
    assert_eq!(
        all.len(),
        4,
        "upsert on an existing id must update, not insert a 5th row"
    );
    assert!(all.contains(&updated));
}

#[test]
fn delete_session_removes_exactly_one_fixture_row() {
    let (db, _dir) = open_fixture_copy("delete_session");
    db.delete_session("22222222-2222-2222-2222-222222222222")
        .unwrap();
    let remaining: Vec<String> = db
        .all_sessions()
        .unwrap()
        .into_iter()
        .map(|s| s.id)
        .collect();
    assert_eq!(
        remaining,
        vec![
            "11111111-1111-1111-1111-111111111111",
            "33333333-3333-3333-3333-333333333333",
            "44444444-4444-4444-4444-444444444444",
        ]
    );
}

/// Exercises `SessionDatabase` purely through the `SessionPersisting`
/// trait object -- the interface call sites are meant to depend on (see
/// `store::persisting`'s doc comment) -- to confirm the trait impl actually
/// wires up to the same fixture-backed behavior as the inherent methods
/// used by every other test in this file.
#[test]
fn all_8_operations_are_reachable_through_the_session_persisting_trait() {
    let (db, _dir) = open_fixture_copy("via_trait");
    let persisting: &dyn SessionPersisting = &db;

    assert_eq!(persisting.all_sessions().unwrap(), expected_sessions());
    assert_eq!(
        persisting.selected_session_id().unwrap(),
        Some("11111111-1111-1111-1111-111111111111".to_string())
    );
    assert_eq!(
        persisting.app_state("other.unrelated.key").unwrap(),
        Some("unrelated-value".to_string())
    );
    assert_eq!(
        persisting
            .app_state_entries("tab.pane.layout.")
            .unwrap()
            .len(),
        3
    );

    persisting.set_app_state(Some("v"), "trait.key").unwrap();
    assert_eq!(
        persisting.app_state("trait.key").unwrap(),
        Some("v".to_string())
    );

    persisting
        .set_selected_session_id(Some("22222222-2222-2222-2222-222222222222"))
        .unwrap();
    assert_eq!(
        persisting.selected_session_id().unwrap(),
        Some("22222222-2222-2222-2222-222222222222".to_string())
    );

    let record = SessionRecord::new(
        "77777777-7777-7777-7777-777777777777",
        "/tmp/seven",
        "seven",
        None,
        utc(2027, 1, 1, 0, 0, 0, 0),
        50,
        None,
        None,
        None,
    );
    persisting.upsert(&record).unwrap();
    assert!(persisting.all_sessions().unwrap().contains(&record));
    persisting.delete_session(&record.id).unwrap();
    assert!(!persisting
        .all_sessions()
        .unwrap()
        .iter()
        .any(|s| s.id == record.id));
}

fn raw_grdb_migrations(path: &Path) -> Vec<String> {
    let conn = rusqlite::Connection::open(path).unwrap();
    let mut stmt = conn
        .prepare("SELECT identifier FROM grdb_migrations ORDER BY identifier")
        .unwrap();
    stmt.query_map([], |row| row.get::<_, String>(0))
        .unwrap()
        .collect::<Result<Vec<_>, _>>()
        .unwrap()
}
