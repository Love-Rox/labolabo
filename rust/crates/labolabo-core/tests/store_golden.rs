//! Golden-oracle test for `labolabo_core::store`: opens a fixture SQLite
//! database that was **actually written by GRDB** (via the real
//! `LaboLaboStore.SessionDatabase`, not hand-authored) and exercises every
//! `SessionPersisting` operation against it, asserting the read results
//! match what was written and that a subsequent write round-trips through
//! `sqlite3` in GRDB's exact on-disk format.
//!
//! Unlike `tests/golden.rs`/`tests/tiling_golden.rs` (which compare
//! canonical JSON renderings against a byte-identical Swift-produced
//! expected file), this crate's operations here are database reads/writes,
//! not a pure function of a string -> JSON. So "golden" here means: the
//! fixture *file* is the oracle output (produced once by real GRDB), and
//! this test's hardcoded expected values are what a human verified that
//! fixture contains (via `sqlite3 fixture.db` — see the dump in this file's
//! comments below each assertion). There is no Rust-side regeneration step
//! for the fixture: it is regenerated by rerunning the Swift oracle
//! described next, not by anything in this repo.
//!
//! ## Fixture provenance / regenerating `fixtures/store/fixture.db`
//!
//! `fixtures/store/fixture.db` was produced by a disposable Swift package
//! (not checked in anywhere, per the porting brief's "leave no trace"
//! convention already used by `fixtures/generate.swift` and the wave-3
//! tiling driver) that depends on this repo's real `LaboLaboStore` product
//! (GRDB) via a local SwiftPM path dependency, and calls the real
//! `SessionDatabase.init(url:)` / `.upsert` / `.setSelectedSessionID` /
//! `.setAppState` — i.e. the exact code path a running LaboLabo app uses.
//! To regenerate:
//!
//! ```sh
//! mkdir -p /tmp/store-fixture-gen/Sources/store-fixture-gen
//! cat > /tmp/store-fixture-gen/Package.swift <<'EOF'
//! // swift-tools-version: 6.0
//! import PackageDescription
//! let package = Package(
//!     name: "store-fixture-gen",
//!     platforms: [.macOS(.v14)],
//!     dependencies: [.package(path: "/path/to/labolabo")],  // this repo's root
//!     targets: [.executableTarget(
//!         name: "store-fixture-gen",
//!         // "package:" must match the *directory basename* of the path
//!         // dependency above, not the `Package(name:)` declared inside it
//!         // (SwiftPM resolves local path dependency identity from the
//!         // directory name) -- e.g. "labolabo" for a `~/.../labolabo` checkout.
//!         dependencies: [.product(name: "LaboLaboStore", package: "<dir-basename>")]
//!     )]
//! )
//! EOF
//! # ... write Sources/store-fixture-gen/main.swift building the records
//! # below via SessionRecord(...)/db.upsert/db.setSelectedSessionID/db.setAppState ...
//! cd /tmp/store-fixture-gen && swift run store-fixture-gen \
//!   /path/to/labolabo/rust/crates/labolabo-core/fixtures/store/fixture.db
//! ```
//!
//! The fixture's exact contents (verified via `sqlite3 fixture.db
//! '.schema'` / `SELECT * FROM session` / `SELECT key, value,
//! typeof(value) FROM appState` / `SELECT * FROM grdb_migrations`):
//!
//! - `grdb_migrations`: exactly `v1`, `v2-agentSession`, `v3-adapter` (GRDB
//!   wrote this; this crate must never touch it).
//! - `session`: 4 rows (see `expected_sessions` below) — one fully
//!   populated with Japanese text, one with every optional column NULL, one
//!   with quotes/backslash/newline/tab/emoji content (parameter-binding
//!   correctness, not string interpolation), one with an exact-midnight
//!   `addedAt`. Inserted out of `sortOrder` order, to exercise `ORDER BY`.
//! - `appState`: `selectedSession` -> the first session's id; three
//!   `tab.pane.layout.*`-prefixed entries (one of them an **empty string**,
//!   which must be kept, distinct from...) a fourth `tab.pane.layout.*` key
//!   whose `value` is SQL **NULL** (which `app_state_entries` must drop);
//!   and one `other.unrelated.key` outside the prefix.

use std::path::{Path, PathBuf};

use chrono::{TimeZone, Utc};
use labolabo_core::store::{SessionDatabase, SessionPersisting, SessionRecord};

fn fixture_path() -> PathBuf {
    Path::new(env!("CARGO_MANIFEST_DIR")).join("fixtures/store/fixture.db")
}

/// Copies the checked-in fixture to a scratch path so tests that write
/// (`upsert`/`delete_session`/`set_app_state`) never mutate the committed
/// fixture file, and don't stomp on each other when tests run in parallel.
fn open_fixture_copy(test_name: &str) -> (SessionDatabase, PathBuf) {
    let dir = std::env::temp_dir().join(format!(
        "labolabo-store-golden-{}-{}",
        std::process::id(),
        test_name
    ));
    std::fs::create_dir_all(&dir).unwrap();
    let path = dir.join("fixture.db");
    std::fs::copy(fixture_path(), &path).unwrap();
    let db = SessionDatabase::open(&path).unwrap();
    (db, path)
}

fn utc(y: i32, mo: u32, d: u32, h: u32, mi: u32, s: u32, ms: u32) -> chrono::DateTime<Utc> {
    Utc.with_ymd_and_hms(y, mo, d, h, mi, s).unwrap()
        + chrono::TimeDelta::milliseconds(i64::from(ms))
}

